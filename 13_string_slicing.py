name = "prachi"
print(name[0])#return first character
print(name[4])
print(name[0:5])#index in string starts from zero to n-1
print(name[2:5])
print(name[-5:-1])
print(name[:4])#it take minimum index by default before : which is zero
print(name[0:])#it takes last index of our string

